# to check type of variable in python we use type

# string data type
a = "Akshat"
# numeric data type
b = 1
# boolean data type
c = True

# sequence data type

# list
a1 = [b, 23, 34, 4, [21, 232]]

# tuple
a2 = (b, 23, 34, 4, (21, 232))

# dictionary
a3 = {"name": "Akshat", "City": "yum"}
print(type(a))
print(type(b))
print(type(c))
print(type(a1))
print(type(a2))
print(type(a3))
